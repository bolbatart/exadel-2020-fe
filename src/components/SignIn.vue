<template>
  <!-- <v-row justify="center"> -->
    <v-dialog v-model="dialog" persistent max-width="600px">
      <template v-slot:activator="{ on }">
        <v-btn class="white black--text" depressed small v-on="on"> Sign In </v-btn>
      </template>
      <v-card>
        <v-card-title>
          <span class="headline">Sign In</span>
        </v-card-title>
        <v-card-text>
          <v-container>
            <v-row>
              <v-col cols="12">
                <v-text-field label="Email" required v-model="loginData.email"></v-text-field>
              </v-col>
              <v-col cols="12">
                <v-text-field label="Password" type="password" required v-model="loginData.password"></v-text-field>
              </v-col>
            </v-row>
          </v-container>
          <p class="caption font-weight-bold"> {{ $store.state.user.login.loginError }} </p>
        </v-card-text>
        <v-card-actions>
          <v-spacer></v-spacer>
          <v-btn color="blue darken-1" text @click="dialog = false">Close</v-btn>
          <v-btn color="blue darken-1" text @click="SignIn()">Sign In</v-btn>
        </v-card-actions>
      </v-card>
    </v-dialog>
  <!-- </v-row> -->
</template>

<script>
  export default {
    data () {
      return {
        dialog: false,
        loginData: {
          email: '',
          password: ''
        }
      }
    },
    methods: {
      SignIn () {
        this.$store.dispatch('LOGIN', this.loginData)
          .then(res => {
            this.dialog = false;
            this.$router.push({ name: 'user-profile' })
          })
      }
    }
  }
</script>